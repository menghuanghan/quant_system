def get_sw_l1_from_industry(industry: str) -> str:
    """
    根据 Tushare industry 字段获取申万一级行业
    
    Args:
        industry: Tushare stock_list_a 中的 industry 字段值
        
    Returns:
        申万一级行业名称，如果无法映射则返回 '未分类'
    """
    if not industry or industry == '未分类':
        return '未分类'
    return INDUSTRY_TO_SW_L1_MAP.get(industry, '未分类')


